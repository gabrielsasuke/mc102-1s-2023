a = int(input("Digite o valor de a: "))
b = int(input("Digite o valor de b: "))

print("a =", a)
print("b =", b)
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a ** b =", a ** b)
print("a // b =", a // b)
print("a % b =", a % b)
